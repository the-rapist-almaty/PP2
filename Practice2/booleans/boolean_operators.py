x = True
y = False

# AND operator: True if both are True
print(x and y)   # False

# OR operator: True if at least one is True
print(x or y)    # True

# NOT operator: flips the boolean value
print(not x)     # False
print(not y)     # True

# Combining comparisons with boolean operators
age = 25
income = 5000

# Check if age is > 18 AND income > 3000
print(age > 18 and income > 3000)  # True

# Check if age is < 18 OR income > 6000
print(age < 18 or income > 6000)   # False

